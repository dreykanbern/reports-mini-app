/**
 * Тип для описания события
 */
export type Event = {
  __1?: string; // Наименование события
  __2?: string; // Время начала
  __3?: number; // Длительность
  __4?: string | string[]; // Спикеры
  __5?: string | string[]; // Спикеры (фото)
  __6?: string; // Вид события
  __7?: string; // Язык
  __8?: string; // Направление (тема)
};
